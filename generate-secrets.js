function validatePassword(password) {
  if (password.length < 8) {
    return 'Password must be at least 8 characters';
  }
  if (!/[A-Z]/.test(password)) {
    return 'Password should contain at least one uppercase letter';
  }
  if (!/[0-9]/.test(password)) {
    return 'Password should contain at least one number';
  }
  return null;
}

async function promptForPassword() {
  return new Promise((resolve) => {
    rl.question(colors.yellow + '🔐 Enter admin password (min 8 chars, with uppercase & number): ' + colors.reset, (password) => {
      const validationError = validatePassword(password);
      if (validationError) {
        log('red', `❌ ${validationError}`);
        resolve(promptForPassword());
      } else {
        rl.question(colors.yellow + '🔐 Confirm password: ' + colors.reset, (confirm) => {
          if (password === confirm) {
            resolve(password);
          } else {
            log('red', '❌ Passwords do not match');
            resolve(promptForPassword());
          }
        });
      }
    });
  });
}

async function promptForVar(varName, defaultValue = '', description = '') {
  return new Promise((resolve) => {
    const desc = description ? ` (${description})` : '';
    const prompt = defaultValue 
      ? `${varName}${desc} [${defaultValue}]: `
      : `${varName}${desc}: `;
    
    rl.question(colors.cyan + prompt + colors.reset, (value) => {
      resolve(value || defaultValue);
    });
  });
}

async function promptForRedis() {
  log('cyan', '\n📀 Configuring Redis (optional)...');
  const useRedis = await promptForVar('Use Redis for session storage?', 'no');
  
  if (useRedis.toLowerCase().startsWith('y')) {
    const env = {};
    env.REDIS_HOST = await promptForVar('REDIS_HOST', 'localhost');
    env.REDIS_PORT = await promptForVar('REDIS_PORT', '6379');
    
    const usePassword = await promptForVar('Set Redis password?', 'yes');
    if (usePassword.toLowerCase().startsWith('y')) {
      env.REDIS_PASSWORD = generateRedisPassword();
      log('green', `✅ Generated Redis password: ${env.REDIS_PASSWORD}`);
    }
    
    // Generate Redis URL
    if (env.REDIS_PASSWORD) {
      env.REDIS_URL = `redis://default:${env.REDIS_PASSWORD}@${env.REDIS_HOST}:${env.REDIS_PORT}`;
    } else {
      env.REDIS_URL = `redis://${env.REDIS_HOST}:${env.REDIS_PORT}`;
    }
    
    return env;
  }
  return {};
}

async function main() {
  const args = process.argv.slice(2);
  
  // Check for help flag
  if (args.includes('--help')) {
    showHelp();
  }
  
  // Check for example flag
  if (args.includes('--example')) {
    createEnvExample();
    process.exit(0);
  }
  
  const writeToFile = args.includes('--write');
  const forceOverwrite = args.includes('--force');
  const configureRedis = args.includes('--redis');
  
  // Get password from command line or prompt
  let password = args.find(arg => !arg.startsWith('--'));
  
  console.log('\n' + '='.repeat(60));
  log('bright', '🔐 REDIRECTOR PRO - SECURE SECRETS GENERATOR');
  console.log('='.repeat(60) + '\n');

  // Generate secrets
  log('cyan', '📡 Generating secure values...\n');
  
  const sessionSecret = generateSessionSecret();
  const metricsKey = generateMetricsKey();
  
  // Get password
  if (!password) {
    password = await promptForPassword();
  } else {
    const validationError = validatePassword(password);
    if (validationError) {
      log('red', `❌ ${validationError}`);
      process.exit(1);
    }
  }
  
  const passwordHash = generatePasswordHash(password);
  
  // Display generated values
  log('green', '✅ Generated successfully!\n');
  
  console.log(colors.bright + 'SESSION_SECRET=' + colors.reset + sessionSecret);
  console.log(colors.bright + 'METRICS_API_KEY=' + colors.reset + metricsKey);
  console.log(colors.bright + 'ADMIN_PASSWORD_HASH=' + colors.reset + passwordHash);
  console.log(colors.dim + '(Password: ' + password + ')' + colors.reset);
  
  console.log('\n' + '='.repeat(60));
  
  // Prepare environment object
  const env = readEnvFile();
  
  // Update with generated secrets
  env.SESSION_SECRET = sessionSecret;
  env.METRICS_API_KEY = metricsKey;
  env.ADMIN_PASSWORD_HASH = passwordHash;
  env.ADMIN_USERNAME = env.ADMIN_USERNAME || 'admin';
  
  // Ask about other common variables if not writing to file
  if (!writeToFile) {
    const answer = await promptForVar('Would you like to configure additional variables?', 'no');
    if (answer.toLowerCase().startsWith('y')) {
      env.PORT = await promptForVar('PORT', env.PORT || '10000');
      env.NODE_ENV = await promptForVar('NODE_ENV', env.NODE_ENV || 'production');
      env.TARGET_URL = await promptForVar('TARGET_URL', env.TARGET_URL || 'https://example.com');
      env.LINK_TTL = await promptForVar('LINK_TTL', env.LINK_TTL || '30m');
      env.MAX_LINKS = await promptForVar('MAX_LINKS', env.MAX_LINKS || '1000000');
      env.BOT_URLS = await promptForVar('BOT_URLS', env.BOT_URLS || 'https://www.google.com,https://www.microsoft.com');
      env.IPINFO_TOKEN = await promptForVar('IPINFO_TOKEN', env.IPINFO_TOKEN || '');
      env.LOG_LEVEL = await promptForVar('LOG_LEVEL', env.LOG_LEVEL || 'info');
      env.DEBUG = await promptForVar('DEBUG', env.DEBUG || 'false');
      env.DISABLE_DESKTOP_CHALLENGE = await promptForVar('DISABLE_DESKTOP_CHALLENGE', env.DISABLE_DESKTOP_CHALLENGE || 'false');
    }
  } else {
    // Set defaults for common vars if not present
    if (!env.PORT) env.PORT = '10000';
    if (!env.NODE_ENV) env.NODE_ENV = 'production';
    if (!env.TARGET_URL) env.TARGET_URL = 'https://example.com';
    if (!env.LINK_TTL) env.LINK_TTL = '30m';
    if (!env.MAX_LINKS) env.MAX_LINKS = '1000000';
    if (!env.BOT_URLS) env.BOT_URLS = 'https://www.google.com,https://www.microsoft.com';
    if (!env.LOG_LEVEL) env.LOG_LEVEL = 'info';
    if (!env.DEBUG) env.DEBUG = 'false';
    if (!env.DISABLE_DESKTOP_CHALLENGE) env.DISABLE_DESKTOP_CHALLENGE = 'false';
  }
  
  // Configure Redis if requested
  if (configureRedis) {
    const redisEnv = await promptForRedis();
    Object.assign(env, redisEnv);
  }
  
  // Write to file if requested
  if (writeToFile) {
    writeEnvFile(env, forceOverwrite);
  } else {
    log('yellow', '\n📋 Copy the values above to your .env file');
    
    // Show complete configuration
    console.log('\n' + colors.bright + 'Complete configuration:' + colors.reset);
    console.log('-'.repeat(40));
    Object.entries(env).forEach(([key, value]) => {
      if (key.includes('PASSWORD') || key.includes('SECRET') || key.includes('KEY')) {
        console.log(`${key}=${value.substring(0, 8)}...`);
      } else {
        console.log(`${key}=${value}`);
      }
    });
    console.log('-'.repeat(40));
  }
  
  // Security warnings
  console.log('\n' + '='.repeat(60));
  log('yellow', '⚠️  IMPORTANT SECURITY NOTES:');
  console.log('   • Store these values securely');
  console.log('   • Never commit .env to version control');
  console.log('   • Change default passwords immediately');
  console.log('   • Use different passwords in production');
  console.log('   • For production, set up Redis: npm install redis connect-redis');
  console.log('='.repeat(60) + '\n');
  
  log('green', '✨ Done! Your secure values are ready.\n');
  
  rl.close();
}

// Run the script
main().catch(err => {
  log('red', `\n❌ Error: ${err.message}`);
  process.exit(1);
});
