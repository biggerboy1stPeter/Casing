#!/usr/bin/env node

/**
 * Generate Secrets Script
 * 
 * Run this script to generate secure values for your .env file
 * 
 * Usage:
 *   node generate-secrets.js                    # Interactive mode
 *   node generate-secrets.js mypassword         # Generate with specific password
 *   node generate-secrets.js --write            # Generate and write to .env
 *   node generate-secrets.js --write mypassword # Generate with password and write to .env
 *   node generate-secrets.js --help              # Show help
 */

const crypto = require('crypto');
const bcrypt = require('bcryptjs');
const fs = require('fs');
const path = require('path');
const readline = require('readline');
const os = require('os');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

const ENV_FILE = path.join(process.cwd(), '.env');
const ENV_EXAMPLE_FILE = path.join(process.cwd(), '.env.example');

// Colors for console output
const colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  dim: '\x1b[2m',
  red: '\x1b[31m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m',
  cyan: '\x1b[36m'
};

function log(color, message) {
  console.log(colors[color] + message + colors.reset);
}

function showHelp() {
  console.log(`
${colors.bright}🔐 GENERATE SECRETS SCRIPT${colors.reset}

${colors.bright}Usage:${colors.reset}
  node generate-secrets.js [options] [password]

${colors.bright}Options:${colors.reset}
  --write       Write generated values directly to .env file
  --force       Force overwrite existing values in .env
  --example     Create .env.example file with template
  --redis       Add Redis configuration to .env
  --help        Show this help message

${colors.bright}Examples:${colors.reset}
  node generate-secrets.js
  node generate-secrets.js mypassword
  node generate-secrets.js --write
  node generate-secrets.js --write mypassword
  node generate-secrets.js --force --write
  node generate-secrets.js --example
  node generate-secrets.js --write --redis

${colors.bright}Output:${colors.reset}
  - SESSION_SECRET: Random 32-byte hex string
  - METRICS_API_KEY: Random 32-byte hex string  
  - ADMIN_PASSWORD_HASH: Bcrypt hash of password
  `);
  process.exit(0);
}

function generateSessionSecret() {
  return crypto.randomBytes(32).toString('hex');
}

function generateMetricsKey() {
  return crypto.randomBytes(32).toString('hex');
}

function generatePasswordHash(password) {
  return bcrypt.hashSync(password, 10);
}

function generateRedisPassword() {
  return crypto.randomBytes(16).toString('hex');
}

function readEnvFile() {
  if (!fs.existsSync(ENV_FILE)) {
    return {};
  }
  
  const content = fs.readFileSync(ENV_FILE, 'utf8');
  const env = {};
  
  content.split('\n').forEach(line => {
    // Skip comments and empty lines
    if (line.startsWith('#') || !line.trim()) return;
    
    const match = line.match(/^([^=]+)=(.*)$/);
    if (match) {
      const key = match[1].trim();
      let value = match[2].trim();
      // Remove quotes if present
      value = value.replace(/^["']|["']$/g, '');
      env[key] = value;
    }
  });
  
  return env;
}

function writeEnvFile(env, force = false) {
  let content = '# Generated by generate-secrets.js\n';
  content += `# Created: ${new Date().toISOString()}\n`;
  content += `# Host: ${os.hostname()}\n`;
  content += `# Run 'node generate-secrets.js --help' for more info\n\n`;
  
  // Write in organized sections
  content += '# ─── REQUIRED SECURITY VARIABLES ──────────────────────────────\n';
  const requiredOrder = ['SESSION_SECRET', 'METRICS_API_KEY', 'ADMIN_PASSWORD_HASH', 'ADMIN_USERNAME'];
  requiredOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  content += '\n# ─── SERVER CONFIGURATION ────────────────────────────────────\n';
  const serverOrder = ['PORT', 'NODE_ENV', 'HOST', 'CORS_ORIGIN'];
  serverOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  content += '\n# ─── LINK CONFIGURATION ──────────────────────────────────────\n';
  const linkOrder = ['TARGET_URL', 'LINK_TTL', 'MAX_LINKS', 'BOT_URLS'];
  linkOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  content += '\n# ─── REDIS CONFIGURATION ─────────────────────────────────────\n';
  const redisOrder = ['REDIS_URL', 'REDIS_HOST', 'REDIS_PORT', 'REDIS_PASSWORD'];
  redisOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  content += '\n# ─── EXTERNAL SERVICES ───────────────────────────────────────\n';
  const externalOrder = ['IPINFO_TOKEN', 'GA_TRACKING_ID', 'SENTRY_DSN'];
  externalOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  content += '\n# ─── LOGGING ─────────────────────────────────────────────────\n';
  const logOrder = ['LOG_LEVEL', 'DEBUG', 'DISABLE_DESKTOP_CHALLENGE'];
  logOrder.forEach(key => {
    if (env[key]) {
      content += `${key}=${env[key]}\n`;
    }
  });
  
  // Add any remaining variables
  const allKeys = Object.keys(env);
  const usedKeys = [...requiredOrder, ...serverOrder, ...linkOrder, ...redisOrder, ...externalOrder, ...logOrder];
  const extraKeys = allKeys.filter(key => !usedKeys.includes(key));
  
  if (extraKeys.length > 0) {
    content += '\n# ─── ADDITIONAL VARIABLES ───────────────────────────────────\n';
    extraKeys.forEach(key => {
      content += `${key}=${env[key]}\n`;
    });
  }
  
  // Check if file exists and handle force flag
  if (fs.existsSync(ENV_FILE) && !force) {
    log('yellow', '⚠️  .env file already exists. Use --force to overwrite.');
    const backup = ENV_FILE + '.backup';
    fs.copyFileSync(ENV_FILE, backup);
    log('green', `✅ Backup created at ${backup}`);
  }
  
  fs.writeFileSync(ENV_FILE, content);
  log('green', `✅ Secrets written to ${ENV_FILE}`);
}

